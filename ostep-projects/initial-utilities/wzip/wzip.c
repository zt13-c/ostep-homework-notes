#include <stdio.h>   // 提供标准I/O函数：fopen, fgetc, fwrite, printf等
#include <stdlib.h>  // 提供exit函数

/**
 * 写入一个游程编码单元到标准输出
 * 
 * 参数:
 *   count - 连续字符的个数（整数）
 *   ch    - 被重复的字符
 * 
 * 说明: 输出格式为先写4字节整数（计数值），再写1字节字符
 *       这种格式使得wunzip可以准确解码
 */
void write_run(int count, char ch) {
    // fwrite: 将二进制数据写入到文件流
    // 参数1: 数据指针 &count
    // 参数2: 每个元素的大小 sizeof(int) = 4字节
    // 参数3: 元素个数 1
    // 参数4: 目标文件流 stdout（标准输出）
    // 返回值: 成功写入的元素个数
    // 
    // 注意: 这里以二进制方式写入整数，不是文本格式,比如 count=5 时，它会写入4字节的二进制数据 0x05 0x00 0x00 0x00（小端序），而不是字符 '5'。
    // 这在跨平台时需要注意字节序（大端/小端）问题
    fwrite(&count, sizeof(int), 1, stdout);
    
    // 写入字符本身（1字节）
    // stdout是标准输出流，默认是行缓冲模式
    // 但这里写入二进制数据，不会添加额外字符
    fwrite(&ch, sizeof(char), 1, stdout);
}

/**
 * wzip程序 - 实现游程编码（Run-Length Encoding）压缩
 * 
 * 功能: 将输入文件中的连续相同字符压缩为 (计数+字符) 对
 * 例如: "aaaaabbb" -> [5,'a'][3,'b']
 * 
 * 用法: wzip file1 [file2 ...]
 * 输出: 压缩后的二进制数据到stdout
 * 
 * 返回值: 0表示成功，非0表示失败
 */
int main(int argc, char *argv[]) {
    // 检查命令行参数
    // argc: 参数个数（包括程序名本身）
    // argv: 参数字符串数组
    if (argc == 1) {
        // 如果没有指定输入文件，打印用法信息
        // 注意: 这与标准zip不同，wzip要求至少一个输入文件
        printf("wzip: file1 [file2 ...]\n");
        exit(1);  // 退出程序，返回状态码1表示错误
    }
    
    // 状态变量
    int count = 0;    // 当前字符连续出现的次数
    char prev = 0;    // 上一个读取的字符（用于比较）
    
    // 遍历所有输入文件
    // argc从1开始，argv[0]是程序名，argv[1]是第一个文件
    for (int i = 1; i < argc; i++) {
        /**
         * fopen: 打开文件
         * 参数1: 文件路径 argv[i]
         * 参数2: 打开模式 "r" - 只读文本模式
         * 
         * 返回值: FILE* 文件指针，失败返回NULL
         * 
         * 注意: "r"模式在Windows和Linux下对换行符处理不同
         * Windows: \r\n 会被转换为 \n
         * Linux: 保持原样
         * 对于二进制数据应使用"rb"，但这里处理文本，使用"r"可以
         */
        FILE *fp = fopen(argv[i], "r");
        if (fp == NULL) {
            // 如果文件打开失败（不存在、无权限等）
            printf("wzip: cannot open file\n");
            exit(1);
        }
        
        int c;  // 使用int存储fgetc返回值，因为EOF是负数
        /**
         * fgetc: 从文件流读取一个字符
         * 参数: 文件指针 fp
         * 返回值: 读取的字符（转换为unsigned char后转为int）
         *         或 EOF (通常为-1) 表示文件结束或错误
         * 
         * 注意: 每次调用会移动文件位置指示器
         * 读取到文件末尾后，再调用会返回EOF
         */
        while ((c = fgetc(fp)) != EOF) {
            char curr = (char)c;  // 将int安全转换为char
            
            if (count == 0) {
                // 第一次读取，初始化prev和count
                prev = curr;
                count++;
            }
            else if (curr == prev) {
                // 当前字符与上一个相同，增加计数
                count++;
            }
            else {
                // 字符发生变化，写入上一个游程
                write_run(count, prev);
                // 开始新的游程
                prev = curr;
                count = 1;
            }
        }
        
        /**
         * fclose: 关闭文件流
         * 参数: 文件指针
         * 返回值: 0成功，EOF失败
         * 
         * 重要: 
         * 1. 会刷新缓冲区（将缓冲的数据写入文件）
         * 2. 释放文件流占用的资源
         * 3. 确保文件内容完整写入
         * 4. 不关闭文件可能导致资源泄露
         */
        fclose(fp);
    }
    
    // 处理最后一个游程（如果存在）
    // 因为只有在字符变化时才会写入，最后一个游程需要单独处理
    if (count > 0) {
        write_run(count, prev);
    }
    
    /**
     * main函数返回值:
     * 0表示程序正常结束
     * 在Unix/Linux中，父进程可以通过wait()获取这个值
     * 非0值通常表示某种错误状态
     */
    return 0;
}

